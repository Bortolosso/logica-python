numero1 = input("Digite um numero: ")
numero1 = int(numero1)

numero2 = input("Digite um segundo numero: ")
numero2 = int(numero2)

if(numero1 == numero2):#operador de igualdade
    print("O numero %d igual a %d. "%(numero1, numero2))
if(numero1 != numero2):#operador de diferenca
    print("O numero %d diferente a %d. "%(numero1, numero2))
if(numero1 < numero2):#operador de menor
    print("O numero %d menor a %d. "%(numero1, numero2))
if(numero1 > numero2):#operador de maior
    print("O numero %d maior a %d. "%(numero1, numero2))
if(numero1 >= numero2):#operador maior igual que
    print("O numero %d maior ou igual a %d. "%(numero1, numero2))
if(numero1 <= numero2):#operador menor igual que
    print("O numero %d menor ou igual a %d. " % (numero1, numero2))

